import 'dotenv/config';
import { Client } from '@notionhq/client';
import { notionProps } from './notion-props.mjs';
import { queryAll } from '../notion-shared/query-all.mjs';
import { getSelectName } from './notion-helpers.mjs';

const notion = new Client({ auth: process.env.NOTION_API_KEY });
const P = notionProps();

function requireEnv(keys) {
  const missing = keys.filter((k) => !process.env[k]);
  if (missing.length) {
    console.error(JSON.stringify({ ok: false, missing }, null, 2));
    process.exit(1);
  }
}

requireEnv(['NOTION_API_KEY', 'NOTION_EXECUTION_REQUESTS_DB_ID', 'NOTION_EXECUTION_PERMITS_DB_ID']);

const PAGE_SIZE = Number(process.env.NOTION_PAGE_SIZE || 50);

async function countPermitsByStatus(status) {
  let c = 0;
  const filter = { property: P.permitStatus, select: { equals: status } };
  for await (const _ of queryAll(notion, process.env.NOTION_EXECUTION_PERMITS_DB_ID, { filter, page_size: PAGE_SIZE })) {
    c++;
  }
  return c;
}

async function countRequestsByExecutionStatusEquals(status) {
  let c = 0;
  const filter = { property: P.requestExecutionStatus, select: { equals: status } };
  for await (const _ of queryAll(notion, process.env.NOTION_EXECUTION_REQUESTS_DB_ID, { filter, page_size: PAGE_SIZE })) {
    c++;
  }
  return c;
}

async function countRequestsBlocked() {
  let c = 0;
  // Notion filter doesn't support contains on select labels reliably across schemas,
  // so we scan and count those starting with "Blocked".
  for await (const page of queryAll(notion, process.env.NOTION_EXECUTION_REQUESTS_DB_ID, { page_size: PAGE_SIZE })) {
    const s = getSelectName(page.properties?.[P.requestExecutionStatus]);
    if (typeof s === 'string' && s.startsWith('Blocked')) c++;
  }
  return c;
}

function makeDashboardMarkdown(summary) {
  const lines = [];
  lines.push(`# Dead Letter Dashboard`);
  lines.push('');
  lines.push(`Updated (UTC): ${summary.updatedAt}`);
  lines.push('');
  lines.push('## Permits');
  lines.push(`- Failed: ${summary.permits.failed}`);
  lines.push(`- Expired: ${summary.permits.expired}`);
  lines.push('');
  lines.push('## Requests');
  lines.push(`- Failed: ${summary.requests.failed}`);
  lines.push(`- Blocked (any): ${summary.requests.blocked}`);
  lines.push('');
  lines.push('Notes: This dashboard is generated by `scripts/notion-events/dead-letter-dashboard.mjs`.');
  return lines.join('\n');
}

async function updateNotionPage(pageId, markdown) {
  // Replace page content with a single markdown-like paragraph block (Notion doesn't support markdown directly).
  // We keep it simple: one rich_text block.
  await notion.blocks.children.append({
    block_id: pageId,
    children: [
      {
        object: 'block',
        type: 'paragraph',
        paragraph: {
          rich_text: [{ type: 'text', text: { content: markdown.slice(0, 1900) } }],
        },
      },
    ],
  });
}

const summary = {
  updatedAt: new Date().toISOString(),
  permits: {
    failed: await countPermitsByStatus('Failed'),
    expired: await countPermitsByStatus('Expired'),
  },
  requests: {
    failed: await countRequestsByExecutionStatusEquals('Failed'),
    blocked: await countRequestsBlocked(),
  },
};

console.log(JSON.stringify({ ok: true, summary }, null, 2));

const dashboardPageId = process.env.NOTION_DEAD_LETTER_DASHBOARD_PAGE_ID;
if (dashboardPageId) {
  const md = makeDashboardMarkdown(summary);
  await updateNotionPage(dashboardPageId, md);
  console.log(JSON.stringify({ ok: true, updatedPage: dashboardPageId }, null, 2));
}

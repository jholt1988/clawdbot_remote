#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '../..');

const args = process.argv.slice(2);
const runbookArg = args.find((arg) => !arg.startsWith('--'));
const runbookPath = runbookArg
  ? path.resolve(process.cwd(), runbookArg)
  : path.join(repoRoot, 'pms-plans', 'demo-runbook.md');

const evidencePath = path.join(repoRoot, 'pms-plans', 'demo-evidence.md');
const evidenceDir = path.join(repoRoot, 'pms-plans', 'evidence');

const writeOutput = args.includes('--write');
const jsonOutput = args.includes('--json');

function readFileSafe(filePath) {
  try {
    return fs.readFileSync(filePath, 'utf8');
  } catch (err) {
    return null;
  }
}

const runbook = readFileSafe(runbookPath);
if (!runbook) {
  console.error(`[demo-evidence-helper] Runbook not found: ${runbookPath}`);
  process.exit(2);
}

function extractScreenshotTable(md) {
  const start = md.indexOf('## Screenshots Needed');
  if (start === -1) return [];
  const nextHeading = md.indexOf('\n## ', start + 1);
  const section = md.slice(start, nextHeading === -1 ? md.length : nextHeading);
  const rows = section.split('\n').filter((line) => /^\|/.test(line));
  const items = [];
  for (const row of rows) {
    if (/^\|\s*-+\s*\|/.test(row)) continue;
    const match = row.match(/^\|\s*([^|]+)\s*\|\s*([^|]+)\s*\|/);
    if (!match) continue;
    const scene = match[1].trim();
    const label = match[2].trim();
    if (!scene || !label || /Scene/i.test(scene)) continue;
    items.push({ scene, label });
  }
  return items;
}

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-+/g, '-');
}

function buildFilename(scene, label) {
  const sceneSlug = slugify(scene.replace(/\./g, '-')) || 'scene';
  const labelSlug = slugify(label).slice(0, 60) || 'shot';
  return `${sceneSlug}-${labelSlug}.png`;
}

const screenshots = extractScreenshotTable(runbook);

if (!fs.existsSync(evidenceDir)) {
  fs.mkdirSync(evidenceDir, { recursive: true });
}

const evidenceItems = screenshots.map((item) => {
  const filename = buildFilename(item.scene, item.label);
  const basePath = path.join('pms-plans', 'evidence', filename);
  const candidates = [filename, filename.replace(/\.png$/i, '.jpg'), filename.replace(/\.png$/i, '.jpeg')];
  const existing = candidates.find((candidate) => fs.existsSync(path.join(evidenceDir, candidate)));
  return {
    ...item,
    filename: existing ? existing : filename,
    status: existing ? 'captured' : 'pending',
  };
});

const summary = evidenceItems.reduce(
  (acc, item) => {
    acc[item.status] = (acc[item.status] || 0) + 1;
    return acc;
  },
  {}
);

if (jsonOutput) {
  console.log(JSON.stringify({ runbook: runbookPath, evidenceDir, summary, evidenceItems }, null, 2));
  process.exit(0);
}

const lines = [];
lines.push('# PMS MVP Demo Evidence (PMS-A-10)');
lines.push('');
lines.push('**Purpose:** Track required demo screenshots and their capture status.');
lines.push('');
lines.push('## Evidence Checklist');
lines.push('');
lines.push(`Runbook: ${path.relative(repoRoot, runbookPath)}`);
lines.push(`Evidence folder: ${path.relative(repoRoot, evidenceDir)}`);
lines.push('');
lines.push(`Summary: captured=${summary.captured || 0} pending=${summary.pending || 0}`);
lines.push('');
lines.push('| Scene | Screenshot | Expected Filename | Status |');
lines.push('|---|---|---|---|');
for (const item of evidenceItems) {
  lines.push(`| ${item.scene} | ${item.label} | ${path.join('evidence', item.filename)} | ${item.status} |`);
}
lines.push('');
lines.push('## Capture Notes');
lines.push('');
lines.push('- Default format: PNG (JPG/JPEG also accepted).');
lines.push('- Include visible URL or page header in each screenshot for traceability.');
lines.push('- Mobile evidence should use DevTools device emulation (iPhone 14 Pro 393×852, Pixel 7 412×915).');
lines.push('');
lines.push('---');
lines.push('Generated by `node scripts/pms/demo-evidence-helper.mjs --write`.');
lines.push('');

const output = lines.join('\n');

if (writeOutput) {
  fs.writeFileSync(evidencePath, output, 'utf8');
}

console.log(output);
